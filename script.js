function createPeaksFromWaveform(waveform) {
    const numOfChannels = 2;
    const length = waveform.sections.flat().length;
    const peaks = Array.from({ length: numOfChannels }, () => new Float32Array(length));

    let sampleIndex = 0;
    waveform.sections.forEach(section => {
        section.forEach(([leftSample, rightSample]) => {
            peaks[0][sampleIndex] = leftSample / 100.0; // left
            peaks[1][sampleIndex] = rightSample / 100.0; // right
            sampleIndex++;
        });
    });
    console.log(peaks)

    return peaks;
}


document.getElementById('playButton').addEventListener('click', () => {
    if (!audioContext) {
        audioContext = new (window.AudioContext || window.webkitAudioContext)();

        wavesurfer = WaveSurfer.create({
            container: '#waveform',
            // audioContext: audioContext,
            audioRate: 1,
            splitChannels: false,
            height: 250,
            normalize: false,
            barWidth: 2,
            barGap: 1,
            progressColor: "#4e495f",
            waveColor: "#4e495f",// #e0dae2
            cursorColor: "red",
            minPxPerSec: 0.1,
            dragToSeek: true,
            peaks: createPeaksFromWaveform(waveform),
            duration: 273,
            media: audio,
            plugins: [WaveSurfer.Timeline.create({
                container: '#waveform-timeline',
                // timeInterval: 60,
                primaryLabelInterval: 60,
                // primaryLabelSpacing: 60,
                secondaryLabelInterval: 15,
                // secondaryLabelSpacing: 15,
                formatTimeCallback: function(seconds) {
                    let minutes = Math.floor(seconds / 60);
                    return `${minutes}:00`;
                },
                style: {
                    fontSize: '14px',
                    color: '#ffffff',
                },
            })],
        });
    }

    audioContext.resume().then(() => {
        wavesurfer.playPause();
    });

    /** On audio position change, fires continuously during playback */
    wavesurfer.on('timeupdate', (currentTime) => {
        console.log('Time', currentTime + 's')
    })

    /** When the user seeks to a new position */
    wavesurfer.on('seeking', (currentTime) => {
        console.log('Seeking', currentTime + 's')
    })

    /** When the user interacts with the waveform (i.g. clicks or drags on it) */
    wavesurfer.on('interaction', (newTime) => {
        console.log('Interaction', newTime + 's')
    })

    /** When the user clicks on the waveform */
    wavesurfer.on('click', (relativeX) => {
        console.log('Click', relativeX)
    })

    /** When the user drags the cursor */
    wavesurfer.on('drag', (relativeX) => {
        console.log('Drag', relativeX)
    })

    /** When the waveform is scrolled (panned) */
    wavesurfer.on('scroll', (visibleStartTime, visibleEndTime) => {
        console.log('Scroll', visibleStartTime + 's', visibleEndTime + 's')
    })

    forwardButton.onclick = () => {
        wavesurfer.skip(5)
    }

    backButton.onclick = () => {
        wavesurfer.skip(-5)
    }
});